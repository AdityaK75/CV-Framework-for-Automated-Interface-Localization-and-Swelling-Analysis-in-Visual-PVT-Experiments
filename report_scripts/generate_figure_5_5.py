import cv2
import matplotlib.pyplot as plt
import matplotlib.patches as mpatches
import matplotlib.lines as mlines
import os
import numpy as np

# --- Use the real pipeline outputs ---
IMG_A = 'results/Report_Air_t10_img_5.png'
IMG_B = 'results/Report_Air_t10_img_4_corrected.png'
OUTPUT_PATH = 'results/annotated_output_figure5_5.png'

def generate_figure():
    for p in [IMG_A, IMG_B]:
        if not os.path.exists(p):
            print(f"Error: could not find {p}")
            return

    t0  = cv2.cvtColor(cv2.imread(IMG_A), cv2.COLOR_BGR2RGB)
    t10 = cv2.cvtColor(cv2.imread(IMG_B), cv2.COLOR_BGR2RGB)

    # --- Layout: two annotated images side-by-side + shared legend below ---
    fig = plt.figure(figsize=(13, 12))
    gs  = fig.add_gridspec(2, 2,
                           height_ratios=[20, 1],
                           hspace=0.08, wspace=0.06)

    ax0  = fig.add_subplot(gs[0, 0])
    ax10 = fig.add_subplot(gs[0, 1])
    ax_legend = fig.add_subplot(gs[1, :])

    ax0.imshow(t0)
    ax0.set_title('(a)  Img 5  |  Height: 48.31 mm  |  Volume: 7.69 ml',
                  fontsize=12, fontweight='bold', pad=6)
    ax0.axis('off')

    ax10.imshow(t10)
    ax10.set_title('(b)  Img 4 (corrected)  |  Height: 68.56 mm  |  Volume: 10.95 ml',
                   fontsize=12, fontweight='bold', pad=6)
    ax10.axis('off')

    # --- Shared legend ---
    legend_elements = [
        mpatches.Patch(edgecolor='#00FF00', facecolor='none', linewidth=2,
                       label='ROI bounding box (green)'),
        mlines.Line2D([], [], color='#FFA500', linewidth=1.5, linestyle='--',
                      label='Vertical axis / centreline (orange dashed)'),
        mlines.Line2D([], [], color='#00FFFF', linewidth=2,
                      label='Meniscus interface line — polynomial fit (cyan)'),
        mpatches.Patch(facecolor='#FF00FF', alpha=0.6,
                       label='Detected edge points (magenta scatter)'),
        mpatches.Patch(facecolor='#1a1a2e', edgecolor='white', linewidth=1,
                       label='Metadata header — sample, timestamp, height, volume'),
    ]

    ax_legend.axis('off')
    ax_legend.legend(handles=legend_elements,
                     loc='center', ncol=3,
                     fontsize=10, frameon=True,
                     framealpha=0.95,
                     edgecolor='#cccccc')

    fig.suptitle('Figure 5.5 — Annotated output generated by the PVT computer-vision pipeline',
                 fontsize=13, y=0.995, fontweight='bold')

    os.makedirs('results', exist_ok=True)
    plt.savefig(OUTPUT_PATH, dpi=300, bbox_inches='tight')
    print(f"Success! Figure saved to: {OUTPUT_PATH}")

if __name__ == "__main__":
    generate_figure()
